    private void tblListaDadosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListaDadosMousePressed

        try {
            // Recupera a linha

            int linha = tblListaDados.getSelectedRow();
           
            String codigo = tblListaDados.getValueAt(linha, 0).toString();
            String descricao = tblListaDados.getValueAt(linha, 1).toString();
            String valorMensalidade = tblListaDados.getValueAt(linha, 2).toString();

            txtCodigo.setText(codigo);
            txtDescricao.setText(descricao);
            txtValorMensalidade.setText(valorMensalidade);
           
            btnExcluir.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tblListaDadosMousePressed
public void carregarTable() throws SQLException {
    
        Vector<String> cabecalho = new Vector<String>();
        cabecalho.add("Codigo");
        cabecalho.add("Descrição");
        cabecalho.add("Valor Mensalidade");

        NTipoAssociado negocio = new NTipoAssociado();
        ETipoAssociado parametro = new ETipoAssociado();
        Vector detalhe = new Vector();

        for (ETipoAssociado associado : negocio.listar()) {
            Vector<String> linha = new Vector<>();
            linha.add(associado.getCodigo() + "");
            linha.add(associado.getDescricao());
            linha.add(associado.getValorMensalidade() + "");

            detalhe.add(linha);

            // Adicionar JTable da tela
            tblListaDados.setModel(new DefaultTableModel(detalhe, cabecalho));

        }
    }
    public void limpar() throws SQLException {
        
        
        txtCodigo.setText("");
        txtDescricao.setText("");
        txtValorMensalidade.setText("");
        btnExcluir.setEnabled(false);
        
        
        
        try {
            carregarTable();
        } catch (SQLException e) {
            Logger.getLogger(frmTipoAssociadoCadastro.class.getName()).log(Level.SEVERE, null, e);
        }
    }


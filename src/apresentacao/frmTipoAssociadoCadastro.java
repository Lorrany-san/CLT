    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {
            ETipoAssociado tipo = new ETipoAssociado();
            if (!txtCodigo.getText().isEmpty()) {
                tipo.setCodigo(Integer.parseInt(txtCodigo.getText()));
            }
            
            tipo.setDescricao(txtDescricao.getText());
            tipo.setValorMensalidade(Double.parseDouble(txtValorMensalidade.getText()));
            
            
            new NTipoAssociado().salvar(tipo);
            
            JOptionPane.showMessageDialog(null, "Operação realizada com "
                    + " sucesso!");
            //Limpar tela
            limpar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println(e);
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try {
            int resposta = JOptionPane.showConfirmDialog(null, "Confirma a exclusão do dados ?"
                , "Agenda de Dados", JOptionPane.YES_NO_OPTION);
            
            
            if (resposta == JOptionPane.YES_NO_OPTION) {
                int codigo = Integer.parseInt(txtCodigo.getText());
                
                new NTipoAssociado().excluir(codigo);
                
                JOptionPane.showMessageDialog(null, "Exclusão efetuada com sucesso!");
                limpar();
            }

        } catch (SQLException e) {

            
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

